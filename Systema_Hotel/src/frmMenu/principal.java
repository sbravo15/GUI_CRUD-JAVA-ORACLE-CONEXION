    public void ListarDatos(){
       clientes=db.ListaCliente();
       DefaultTableModel tb=(DefaultTableModel)tblTabla.getModel();
       for(Cliente cl: clientes){
           tb.addRow(new Object[]{cl.getID_CLIENTE(),cl.getPRIMER_NOMBRE(),cl.getAPELLIDO(),cl.getEMAIL(),cl.getNUM_TELEFONO(),cl.getID_RESERVACION()});
           
       }
    }
       public void LimpiarFormulario(){
           DefaultTableModel tb=(DefaultTableModel)tblTabla.getModel();
           for(int i=tb.getRowCount()-1;i>=0;i--)
               tb.removeRow(i);
    }
       
    
