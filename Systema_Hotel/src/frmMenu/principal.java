    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        Cliente cl=new Cliente();
        cl.setID_CLIENTE(Integer.parseInt(txtId.getText()));
        cl.setPRIMER_NOMBRE(txtNombre.getText());
        cl.setAPELLIDO(txtApellido.getText());
        cl.setEMAIL(txtEmail.getText());
        cl.setNUM_TELEFONO(txtTelefono.getText());
        cl.setID_RESERVACION(Integer.parseInt(txtIdReservacion.getText()));
        if(!"".equals(txtNombre.getText())&&!"".equals(txtApellido.getText())&&!"".equals(txtEmail.getText())&&!"".equals(txtTelefono.getText())){
            JOptionPane.showMessageDialog(this,"Datos Ingresados","",JOptionPane.INFORMATION_MESSAGE);
            db.insertarClientes(cl);
            LimpiarFormulario();
            ListarDatos();
    }else{
            JOptionPane.showMessageDialog(this,"Falta Ingresar Datos","",JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

